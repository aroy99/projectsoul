package komorebi.projsoul.entities.sprites;

/**
 * Contains the texture coordinates and size of one image
 *
 * @author Aaron Roy
 */
public class Sprite {
  public final int tx, ty, sx, sy;
  
  public Sprite(int tx, int ty, int sx, int sy){
    this.tx = tx;
    this.ty = ty;
    this.sx = sx;
    this.sy = sy;
  }
  
  
}
